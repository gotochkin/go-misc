/// Example of parsing different arguments including an array of values

package main

import (
	//"flag"
	"fmt"
	"io/ioutil"
	"log"
	"net/http"
	"os"
)
func main(){
	//
	response, err := http.Get("http://pokeapi.co/api/v2/pokedex/kanto/")
	if err != nil {
		fmt.Print(err.Error())
		os.Exit(1)
	}
	responseData, err := ioutil.ReadAll(response.Body)
	if err != nil {
		log.Fatal(err)
	}
	fmt.Println(responseData)
}